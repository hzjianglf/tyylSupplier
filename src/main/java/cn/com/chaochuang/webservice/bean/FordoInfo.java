package cn.com.chaochuang.webservice.bean;

import java.util.Date;

/**
 * @author huangwq
 *
 */
public class FordoInfo {
    private Long   rmPendingId;
    /** 标题 */
    private String title;

    /** 发送人姓名 */
    private String senderName;

    /** 接收人 */
    private Long   recipientId;

    /** 发送时间 */
    private Date   sendTime;

    /**
     * @return the rmPendingId
     */
    public Long getRmPendingId() {
        return rmPendingId;
    }

    /**
     * @param rmPendingId
     *            the rmPendingId to set
     */
    public void setRmPendingId(Long rmPendingId) {
        this.rmPendingId = rmPendingId;
    }

    /**
     * @return the title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title
     *            the title to set
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * @return the senderName
     */
    public String getSenderName() {
        return senderName;
    }

    /**
     * @param senderName
     *            the senderName to set
     */
    public void setSenderName(String senderName) {
        this.senderName = senderName;
    }

    /**
     * @return the recipientId
     */
    public Long getRecipientId() {
        return recipientId;
    }

    /**
     * @param recipientId
     *            the recipientId to set
     */
    public void setRecipientId(Long recipientId) {
        this.recipientId = recipientId;
    }

    /**
     * @return the sendTime
     */
    public Date getSendTime() {
        return sendTime;
    }

    /**
     * @param sendTime
     *            the sendTime to set
     */
    public void setSendTime(Date sendTime) {
        this.sendTime = sendTime;
    }

}
